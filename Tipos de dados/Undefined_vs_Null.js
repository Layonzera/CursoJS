/*
    * undefined // algo que realmente não vai existir!
        * indefinido

    * null // algo que existe mas não tem nada dentro dele!
        * nulo
        * objeto que não possui nada dentro
        *  diferente de indefinido

*/
console.log(undefined)
console.log(null)
console.log(null === undefined) // null não é igual a undefined. São duas coisas diferentes!
